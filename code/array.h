#include<iostream>
using namespace std;
//没有引用数组：引用是不能赋值的，数组中的元素必须可以赋值
//字符数组：ca2,ca3是c风格字符串
char ca1[] = {'C','+','+'};//末尾没有null字符,长度为3
char ca2[] = {'C','+','+','\0'};//末尾显式添加null，长度为4
char ca3[] = "C++";//末尾自动添加null，长度为4
//编译错误：char ch[3] = "C++";必须至少用4个字符长度来存放
//c风格字符串不行以null结束，只有这样才可以用字符串处理函数：strlen，strcpy,strcat等
//逗号运算符：表达式分别结算，整个表达式的值是最后一个表达式的值，赋值运算符优先级高于逗号运算符
